package com.example.mlchallenge.Model;

public class Product {
    private int id;
    private String title;
    private  String seller;
    private int price;
    private  String condition;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getSeller() {
        return seller;
    }

    public int getPrice() {
        return price;
    }

    public String getCondition() {
        return condition;
    }



}
